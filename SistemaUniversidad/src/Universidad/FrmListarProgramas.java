    public void listarProgramas(){
        
     int cantidad = GestionUniversidad.listaProgramas.size();
        DefaultTableModel modelo = (DefaultTableModel) tblProgramas.getModel();
        tblProgramas.setModel(modelo);
        String[] datos = new String[4];
        for(int i=0;i<cantidad;i++){
            datos[0]=GestionUniversidad.listaProgramas.get(i).getCodigo();
            datos[1]=GestionUniversidad.listaProgramas.get(i).getNombre();
            datos[2]=Integer.toString(GestionUniversidad.listaProgramas.get(i).getHoras());
            datos[3]=GestionUniversidad.listaProgramas.get(i).getUnProfesor().getNombre();
            modelo.addRow(datos);           
        }
    }
