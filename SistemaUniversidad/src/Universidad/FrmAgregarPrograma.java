    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombreCurso.getText().isEmpty() 
                && !txtNumeroHoras.getText().isEmpty()){
            try{
                    String codigoPrograma = txtCodigo.getText().trim();
                    String nombre = txtNombreCurso.getText().trim();
                    boolean existePrograma = GestionUniversidad.existePrograma(codigoPrograma);
                    if(!existePrograma){
                        int horas = Integer.parseInt(txtNumeroHoras.getText().trim());
                        Profesor unProfesor = (Profesor)cbProfesor.getSelectedItem();
                        Programa unPrograma = new Programa(codigoPrograma,nombre,horas,unProfesor);
                        GestionUniversidad.agregarPrograma(unPrograma);
                        JOptionPane.showMessageDialog(null, "Profesor agregado correctamente");
                        txtCodigo.setText("");
                        txtNombreCurso.setText("");
                        txtNumeroHoras.setText("");
                    }else{
                        JOptionPane.showMessageDialog(null, "Ya existe programa con ese codigo");
                    }
                }catch(NumberFormatException | HeadlessException ex){
                    JOptionPane.showMessageDialog(null, "No cumple el formato " + ex.getMessage());
                }
            
        }else{
            JOptionPane.showMessageDialog(null, "Faltan datos, revisar..");
        }        
    }//GEN-LAST:event_btnAgregarActionPerformed

